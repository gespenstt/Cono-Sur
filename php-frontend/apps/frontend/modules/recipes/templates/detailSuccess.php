					  		<h4>CORDERO CON FRITAS EN SALSA FRAMBUESA</h4>

					  		<h3>Ingredients:</h3>

					  		<ul class="list-unstyled">

							 	<li>400 g jauhoista perunaa 1 hapokas omena</li>
								<li>1 pieni sipuli</li>
								<li>1/2 dl vehnäjauhoja</li>
							 	<li>1 kananmuna</li>
							 	<li>1/4 tl leivinjauhetta</li>
							 	<li>3/4 tl suolaa</li>
							 	<li>mustapippuria myllystä</li>

							</ul>

							<h3>Instructions:</h3>

					  		<p class="text-left">Kuori ja raasta peruna, omena ja sipuli. Sekoita raasteet keskenään ja laita ne verkkosiivilään. Laita siivilä kulhon päälle ja anna raasteen valua 20 minuuttia niin, että työnnät välillä raastetta siivilää vasten.</p>

					  		<p class="text-left">Siirrä siivilä syrjään ja kaada varovaisesti kulhoon valunut vesi pois niin, että kulhon pohjalle kerääntynyt perunatärkkelys jää pohjalle. Sekoita tärk- kelykseen raasteet, vehnäjauho, kananmuna ja leivinjauhe. Mausta suolalla ja mustapippurilla.</p>

					  		<p class="text-left">Kuumenna mieluiten valurautaisella paistinpannulla n. puolen senttimetrin kerros rypsiöljyä. Pannu saa olla melko kuuma. Nostele ruokalusikalla raastesekoitusta pannulle, paina litteäksi ja muotoile röstit lusikalla halu- amasi mallisiksi. Paista kullanruskeiksi ja rapeiksi molemmilta puolilta, n. 2-3 minuuttia per puoli.</p>

					  		<p class="text-left">Tarjoile röstit ranskankerman, mädin ja tuoreen timjamin kera.</p>

					  		

